let soyCadena = 'cualquier cadena';
console.log(soyCadena, ' -> ', typeof soyCadena);

let soyBooleano = false;
console.log(soyBooleano, ' -> ', typeof soyBooleano);

// todos los tipos de numeros estan encapsulados como tipo number
let soyNumero = 55;
console.log(soyNumero, ' -> ', typeof soyNumero);

soyNumero = 63.55;
console.log(soyNumero, ' -> ', typeof soyNumero);

// toda variable que no sea un dato primitivo se considera un objeto
let soyNull = null;
console.log(soyNull, ' -> ', typeof soyNull);

let soyUndefined;
console.log(soyUndefined, ' -> ', typeof soyUndefined);